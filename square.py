
def area(a):
    '''
    Return area of square.

        Parameters:
            a (double): Length of side of square.
        
        Return value:
            area (double): Area of square.
    '''
    return a * a


def perimeter(a):
    '''
    Return perimeter of square.

        Parameters:
            a (double): Length of side of square.
        
        Return value:
            perimeter (double): Perimeter of square.
    '''
    return 4 * a
