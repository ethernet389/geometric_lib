
def area(a):
    '''
    Return area of square.

        Parameters:
            a (float): Length of side of square.
        
        Return value:
            area (float): Area of square.
    '''
    return a * a


def perimeter(a):
    '''
    Return perimeter of square.

        Parameters:
            a (float): Length of side of square.
        
        Return value:
            perimeter (float): Perimeter of square.
    '''
    return 4 * a
