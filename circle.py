import math


def area(r):
    '''
    Return area of circle.

        Parameters:
            r (double): Radius of circle.
        
        Return value:
            area (double): Area of circle.
    '''
    return math.pi * r * r


def perimeter(r):
    '''
    Return perimeter of circle.

        Parameters:
            r (double): Radius of circle.
        
        Return value:
            perimeter (double): Perimeter of circle.
    '''
    return 2 * math.pi * r

